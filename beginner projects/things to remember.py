#Somcle more metacharacters are *, +, ?, { and }.
#These specify numbers of repetitions. 
#The metacharacter * means "zero or more repetitions of the previous thing". It tries to match as many repetitions 
#as possible. The "previous thing" can be a single character, a class, or a group of characters in parentheses.
#The metacharacter + is very similar to *, except it means "one or more repetitions", as opposed to "zero or more
#repetitions".
#The metacharacter ? means "zero or one repetitions".